CREATE TABLE IF NOT EXISTS chat.MEMBER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOGIN_ID VARCHAR(30),
    PASSWORD VARCHAR(200),
    NAME VARCHAR(100),
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

CREATE TABLE IF NOT EXISTS chat.CHAT_ROOM (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(100),
    ROOM_TYPE VARCHAR(20) NOT NULL,
    ROOM_KEY VARCHAR(500) NOT NULL UNIQUE,
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

CREATE TABLE IF NOT EXISTS chat.CHAT_ROOM_MEMBER (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHAT_ROOM_ID BIGINT NOT NULL,
    MEMBER_ID BIGINT NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    JOINED_AT TIMESTAMP,
    LEFT_AT TIMESTAMP,
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT UK_CHAT_ROOM_MEMBER UNIQUE (CHAT_ROOM_ID, MEMBER_ID),
    CONSTRAINT FK_CHAT_ROOM_MEMBER_ROOM FOREIGN KEY (CHAT_ROOM_ID) REFERENCES chat.CHAT_ROOM (ID),
    CONSTRAINT FK_CHAT_ROOM_MEMBER_MEMBER FOREIGN KEY (MEMBER_ID) REFERENCES chat.MEMBER (ID)
);

CREATE TABLE IF NOT EXISTS chat.CHAT_MESSAGE (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHAT_ROOM_ID BIGINT NOT NULL,
    SENDER_MEMBER_ID BIGINT NOT NULL,
    MESSAGE_TYPE VARCHAR(20) NOT NULL,
    CONTENT JSONB NOT NULL,
    SENT_AT TIMESTAMP NOT NULL,
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT FK_CHAT_MESSAGE_ROOM FOREIGN KEY (CHAT_ROOM_ID) REFERENCES chat.CHAT_ROOM (ID),
    CONSTRAINT FK_CHAT_MESSAGE_SENDER FOREIGN KEY (SENDER_MEMBER_ID) REFERENCES chat.MEMBER (ID)
);

CREATE INDEX IF NOT EXISTS IDX_CHAT_MESSAGE_ROOM_SENT_AT
ON chat.CHAT_MESSAGE (CHAT_ROOM_ID, SENT_AT);

CREATE TABLE IF NOT EXISTS chat.CHAT_NOTIFICATION (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CHAT_MESSAGE_ID BIGINT NOT NULL,
    RECEIVER_MEMBER_ID BIGINT NOT NULL,
    PAYLOAD JSONB NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    NOTIFIED_AT TIMESTAMP,
    CREATED_AT TIMESTAMP,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT FK_CHAT_NOTIFICATION_MESSAGE FOREIGN KEY (CHAT_MESSAGE_ID) REFERENCES chat.CHAT_MESSAGE (ID),
    CONSTRAINT FK_CHAT_NOTIFICATION_RECEIVER FOREIGN KEY (RECEIVER_MEMBER_ID) REFERENCES chat.MEMBER (ID)
);

CREATE INDEX IF NOT EXISTS IDX_CHAT_NOTIFICATION_RECEIVER_STATUS
ON chat.CHAT_NOTIFICATION (RECEIVER_MEMBER_ID, STATUS);
